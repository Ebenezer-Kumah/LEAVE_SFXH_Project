    <p>Find quick answers to the most common questions about the Employee Leave Management System.</p>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-user-clock"></i> Leave Policies & Procedures</h3>
    </div>
    <div class="card-body">
        <div class="faq-section">
            <div class="faq-item">
                <h4 class="faq-question">How many annual leave days am I entitled to?</h4>
                <div class="faq-answer">
                    <p>Annual leave entitlement depends on your length of service:</p>
                    <ul>
                        <li><strong>0-2 years:</strong> 21 working days per year</li>
                        <li><strong>3-5 years:</strong> 25 working days per year</li>
                        <li><strong>6+ years:</strong> 30 working days per year</li>
                    </ul>
                    <p>Your exact entitlement is shown in your leave balance.</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">Can I carry forward unused leave days?</h4>
                <div class="faq-answer">
                    <p>Yes, you can carry forward up to 5 unused annual leave days to the next year, subject to management approval. Sick leave and other leave types cannot be carried forward.</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">What's the minimum notice period for leave?</h4>
                <div class="faq-answer">
                    <p>You should submit your leave request at least:</p>
                    <ul>
                        <li><strong>Annual Leave:</strong> 14 days in advance</li>
                        <li><strong>Sick Leave:</strong> Same day (with medical certificate)</li>
                        <li><strong>Emergency Leave:</strong> As soon as possible</li>
                        <li><strong>Maternity/Paternity:</strong> 30 days in advance</li>
                    </ul>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">Can I cancel an approved leave request?</h4>
                <div class="faq-answer">
                    <p>Yes, you can cancel approved leave up to 7 days before the start date. After that, cancellation requires manager approval and may affect your leave balance.</p>
                </div>
            </div>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-calendar-check"></i> System Usage</h3>
    </div>
    <div class="card-body">
        <div class="faq-section">
            <div class="faq-item">
                <h4 class="faq-question">How do I check my leave balance?</h4>
                <div class="faq-answer">
                    <p>Your leave balance is displayed on your dashboard. You can also:</p>
                    <ol>
                        <li>Click "Leave Balance" in the sidebar</li>
                        <li>View detailed breakdown by leave type</li>
                        <li>See used and remaining days</li>
                    </ol>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">What happens if I don't have enough leave days?</h4>
                <div class="faq-answer">
                    <p>The system will prevent you from applying for more leave than you have available. If you need additional leave, contact HR to discuss options like unpaid leave or advance leave.</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">How long does leave approval take?</h4>
                <div class="faq-answer">
                    <p>Most leave requests are processed within:</p>
                    <ul>
                        <li><strong>Annual Leave:</strong> 2-3 working days</li>
                        <li><strong>Sick Leave:</strong> 1 working day</li>
                        <li><strong>Emergency Leave:</strong> Same day</li>
                        <li><strong>Study Leave:</strong> 5-7 working days</li>
                    </ul>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">Can I apply for leave while on probation?</h4>
                <div class="faq-answer">
                    <p>Yes, but annual leave during probation is limited to 1.5 days per month of service. Sick leave and emergency leave are available as normal during probation.</p>
                </div>
            </div>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-file-medical"></i> Medical & Sick Leave</h3>
    </div>
    <div class="card-body">
        <div class="faq-section">
            <div class="faq-item">
                <h4 class="faq-question">Do I need a medical certificate for sick leave?</h4>
                <div class="faq-answer">
                    <p>Yes, for sick leave of 3 or more consecutive days, you must provide a medical certificate from a registered medical practitioner. For shorter periods, a self-certification may be acceptable.</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">What's the difference between sick leave and medical leave?</h4>
                <div class="faq-answer">
                    <p><strong>Sick Leave:</strong> For personal illness or injury (up to 30 days per year)<br>
                    <strong>Medical Leave:</strong> For medical appointments, treatments, or procedures (up to 10 days per year)</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">Can sick leave be taken in half days?</h4>
                <div class="faq-answer">
                    <p>Yes, sick leave can be taken in half-day increments. However, you must provide appropriate medical documentation for any sick leave taken.</p>
                </div>
            </div>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-users"></i> Maternity & Family Leave</h3>
    </div>
    <div class="card-body">
        <div class="faq-section">
            <div class="faq-item">
                <h4 class="faq-question">How much maternity leave am I entitled to?</h4>
                <div class="faq-answer">
                    <p>Female employees are entitled to 12 weeks of maternity leave at full pay, plus an additional 4 weeks at half pay. This can be taken before or after childbirth.</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">What about paternity leave?</h4>
                <div class="faq-answer">
                    <p>Male employees are entitled to 5 working days of paternity leave within 2 weeks of the child's birth. This leave is paid at full salary.</p>
                </div>
            </div>

            <div class="faq-item">
                <h4 class="faq-question">Can I work part-time during maternity leave?</h4>
                <div class="faq-answer">
                    <p>No, maternity leave is full-time leave. However, you may be able to arrange flexible working hours upon your return, subject to departmental needs.</p>
                </div>
            </div>
        </div>
    </div>
</div>

