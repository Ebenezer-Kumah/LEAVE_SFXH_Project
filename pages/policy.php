    <p>Comprehensive guide to leave entitlements, procedures, and regulations for St. Francis Xavier Hospital employees.</p>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-calendar-alt"></i> Annual Leave</h3>
    </div>
    <div class="card-body">
        <div class="policy-section">
            <h4>Entitlement</h4>
            <p>Annual leave entitlement is based on length of continuous service:</p>
            <div class="entitlement-table">
                <div class="table-row header">
                    <span>Service Length</span>
                    <span>Annual Leave Days</span>
                </div>
                <div class="table-row">
                    <span>0 - 2 years</span>
                    <span>21 working days</span>
                </div>
                <div class="table-row">
                    <span>3 - 5 years</span>
                    <span>25 working days</span>
                </div>
                <div class="table-row">
                    <span>6+ years</span>
                    <span>30 working days</span>
                </div>
            </div>
        </div>

        <div class="policy-section">
            <h4>Application Procedure</h4>
            <ol>
                <li>Submit leave request through the ELMS system at least 14 days in advance</li>
                <li>Provide detailed reason for leave and planned activities</li>
                <li>Ensure adequate departmental coverage during absence</li>
                <li>Receive approval from immediate supervisor and department head</li>
                <li>Submit any required documentation before leave commencement</li>
            </ol>
        </div>

        <div class="policy-section">
            <h4>Carry Forward</h4>
            <p>Employees may carry forward up to 5 unused annual leave days to the next calendar year, subject to:</p>
            <ul>
                <li>Management approval</li>
                <li>Operational requirements</li>
                <li>Maximum accumulation of 10 days total</li>
            </ul>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-thermometer-half"></i> Sick Leave</h3>
    </div>
    <div class="card-body">
        <div class="policy-section">
            <h4>Entitlement</h4>
            <p>Full-time employees are entitled to:</p>
            <ul>
                <li><strong>Paid Sick Leave:</strong> 30 calendar days per year</li>
                <li><strong>Half-Pay Sick Leave:</strong> Additional 30 calendar days</li>
                <li><strong>Accumulation:</strong> Unused sick leave does not carry forward</li>
            </ul>
        </div>

        <div class="policy-section">
            <h4>Medical Certification</h4>
            <div class="certification-rules">
                <p><strong>Required for:</strong></p>
                <ul>
                    <li>Absence of 3 or more consecutive days</li>
                    <li>Absence exceeding 10 days in a 12-month period</li>
                    <li>Pattern of frequent short-term absences</li>
                    <li>Suspicion of leave abuse</li>
                </ul>
                <p><strong>Certificate must be from:</strong></p>
                <ul>
                    <li>Registered medical practitioner</li>
                    <li>Hospital medical officer</li>
                    <li>Specialist consultant (for specialized conditions)</li>
                </ul>
            </div>
        </div>

        <div class="policy-section">
            <h4>Notification Requirements</h4>
            <ul>
                <li><strong>Same Day:</strong> Notify supervisor by 9:00 AM</li>
                <li><strong>Extended Leave:</strong> Provide medical certificate within 48 hours</li>
                <li><strong>Return to Work:</strong> Submit fitness certificate if absent 7+ days</li>
            </ul>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-heart"></i> Maternity & Paternity Leave</h3>
    </div>
    <div class="card-body">
        <div class="policy-section">
            <h4>Maternity Leave</h4>
            <div class="maternity-info">
                <p><strong>Duration:</strong> 12 weeks full pay + 4 weeks half pay</p>
                <p><strong>Eligibility:</strong> Female employees with 1+ years continuous service</p>
                <p><strong>Timing:</strong> Can be taken before or after childbirth</p>
                <p><strong>Documentation:</strong> Medical certificate confirming pregnancy and expected due date</p>
            </div>
        </div>

        <div class="policy-section">
            <h4>Paternity Leave</h4>
            <div class="paternity-info">
                <p><strong>Duration:</strong> 5 working days</p>
                <p><strong>Eligibility:</strong> Male employees whose spouse has given birth</p>
                <p><strong>Timing:</strong> Must be taken within 2 weeks of childbirth</p>
                <p><strong>Pay:</strong> Full salary during paternity leave</p>
            </div>
        </div>

        <div class="policy-section">
            <h4>Adoption Leave</h4>
            <p>Employees adopting a child under 12 months are entitled to:</p>
            <ul>
                <li>6 weeks paid leave for primary caregiver</li>
                <li>2 weeks paid leave for secondary caregiver</li>
                <li>Additional unpaid leave up to 12 months</li>
            </ul>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-exclamation-triangle"></i> Emergency & Compassionate Leave</h3>
    </div>
    <div class="card-body">
        <div class="policy-section">
            <h4>Emergency Leave</h4>
            <p>May be granted for unforeseen circumstances including:</p>
            <ul>
                <li>Serious illness or accident affecting immediate family</li>
                <li>Death of immediate family member</li>
                <li>Natural disasters affecting employee's residence</li>
                <li>Other exceptional circumstances requiring immediate attention</li>
            </ul>
            <p><strong>Duration:</strong> Up to 5 working days, extendable based on circumstances</p>
        </div>

        <div class="policy-section">
            <h4>Compassionate Leave</h4>
            <div class="compassionate-table">
                <div class="table-row header">
                    <span>Relationship</span>
                    <span>Leave Duration</span>
                </div>
                <div class="table-row">
                    <span>Spouse/Child/Parent</span>
                    <span>7 working days</span>
                </div>
                <div class="table-row">
                    <span>Sibling/Grandparent</span>
                    <span>3 working days</span>
                </div>
                <div class="table-row">
                    <span>Other relatives</span>
                    <span>1 working day</span>
                </div>
            </div>
        </div>

        <div class="policy-section">
            <h4>Documentation Requirements</h4>
            <ul>
                <li>Death certificate for bereavement leave</li>
                <li>Medical certificate for family illness</li>
                <li>Police report for accidents or emergencies</li>
                <li>Other supporting documentation as requested</li>
            </ul>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-graduation-cap"></i> Study & Training Leave</h3>
    </div>
    <div class="card-body">
        <div class="policy-section">
            <h4>Study Leave</h4>
            <p>Available for approved educational programs:</p>
            <ul>
                <li><strong>Short Courses:</strong> Up to 10 working days per year</li>
                <li><strong>Professional Exams:</strong> 5 days per exam (max 15 days/year)</li>
                <li><strong>Degree Programs:</strong> Up to 6 months unpaid leave</li>
                <li><strong>Research Leave:</strong> Up to 12 months (conditions apply)</li>
            </ul>
        </div>

        <div class="policy-section">
            <h4>Training Leave</h4>
            <p>For hospital-sponsored training and development:</p>
            <ul>
                <li><strong>Local Training:</strong> Full pay for duration of training</li>
                <li><strong>International Training:</strong> Full pay up to 3 months</li>
                <li><strong>Conferences:</strong> Up to 7 days per conference</li>
                <li><strong>Workshops:</strong> Up to 5 days per workshop</li>
            </ul>
        </div>

        <div class="policy-section">
            <h4>Approval Process</h4>
            <ol>
                <li>Submit training proposal with benefits to department</li>
                <li>Obtain departmental and HR approval</li>
                <li>Sign training bond if required</li>
                <li>Complete training evaluation upon return</li>
            </ol>
        </div>
    </div>
</div>

