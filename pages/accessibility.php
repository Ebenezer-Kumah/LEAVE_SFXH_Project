<div class="content-card">
    <div class="card-header">
        <h3>Accessibility Standards</h3>
    </div>
    <div class="card-body">
        <div class="accessibility-section">
            <p>We strive to conform to the <strong>Web Content Accessibility Guidelines (WCAG) 2.1</strong> at the AA level. These guidelines explain how to make web content more accessible for people with disabilities, and user friendly for everyone.</p>

            <h4>Key Standards We Follow:</h4>
            <ul class="accessibility-list">
                <li><strong>Perceivable:</strong> Information and user interface components must be presentable to users in ways they can perceive</li>
                <li><strong>Operable:</strong> User interface components and navigation must be operable</li>
                <li><strong>Understandable:</strong> Information and the operation of user interface must be understandable</li>
                <li><strong>Robust:</strong> Content must be robust enough that it can be interpreted reliably by a wide variety of user agents, including assistive technologies</li>
            </ul>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3>Accessibility Features</h3>
    </div>
    <div class="card-body">
        <div class="accessibility-section">
            <p>Our system includes the following accessibility features:</p>

            <h4>Visual Accessibility</h4>
            <ul class="accessibility-list">
                <li>High contrast color schemes</li>
                <li>Scalable text up to 200% without horizontal scrolling</li>
                <li>Clear visual focus indicators</li>
                <li>Consistent navigation and layout</li>
                <li>Alternative text for all images and icons</li>
                <li>Color is not the only means of conveying information</li>
            </ul>

            <h4>Motor Accessibility</h4>
            <ul class="accessibility-list">
                <li>Large clickable areas (minimum 44x44 pixels)</li>
                <li>Keyboard navigation support</li>
                <li>Adequate time limits for form completion</li>
                <li>No seizure-inducing content</li>
                <li>Responsive design for various screen sizes</li>
            </ul>

            <h4>Hearing Accessibility</h4>
            <ul class="accessibility-list">
                <li>Visual alternatives for audio content</li>
                <li>Captions for video content when applicable</li>
                <li>No auto-playing audio content</li>
            </ul>

            <h4>Cognitive Accessibility</h4>
            <ul class="accessibility-list">
                <li>Clear and consistent navigation</li>
                <li>Logical reading and tab order</li>
                <li>Error prevention and clear error messages</li>
                <li>Simple and clear language</li>
                <li>Consistent interaction patterns</li>
            </ul>
        </div>
    </div>
</div>

<div class="content-card">
    <div class="card-header">
        <h3>Keyboard Navigation</h3>
    </div>
    <div class="card-body">
        <div class="accessibility-section">
            <p>Our system can be navigated using only a keyboard. Key navigation features include:</p>
            <ul class="accessibility-list">
                <li><strong>Tab:</strong> Navigate through interactive elements</li>
                <li><strong>Shift + Tab:</strong> Navigate backwards through elements</li>
                <li><strong>Enter/Space:</strong> Activate buttons and links</li>
                <li><strong>Arrow Keys:</strong> Navigate within form controls and menus</li>
                <li><strong>Escape:</strong> Close modals and dropdowns</li>
                <li>Visible focus indicators on all interactive elements</li>
            </ul>
        </div>
    </div>
</div>

