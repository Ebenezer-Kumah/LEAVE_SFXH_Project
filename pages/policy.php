<div class="content-card">
    <div class="card-header">
        <h3><i class="fas fa-balance-scale"></i> Employee Rights & Responsibilities</h3>
    </div>
    <div class="card-body">
        <div class="policy-section">
            <h4>Employee Rights</h4>
            <ul>
                <li>Take entitled leave without fear of reprisal</li>
                <li>Receive fair and timely processing of leave requests</li>
                <li>Appeal leave decisions through proper channels</li>
                <li>Maintain confidentiality of medical information</li>
                <li>Return to same or equivalent position after leave</li>
            </ul>
        </div>

        <div class="policy-section">
            <h4>Employee Responsibilities</h4>
            <ul>
                <li>Submit leave requests in a timely manner</li>
                <li>Provide accurate information and documentation</li>
                <li>Ensure adequate handover before leave</li>
                <li>Remain contactable during leave if required</li>
                <li>Return to work on scheduled date</li>
                <li>Report extended illness promptly</li>
            </ul>
        </div>

        <div class="policy-section">
            <h4>Appeals Process</h4>
            <p>If you disagree with a leave decision:</p>
            <ol>
                <li>Discuss with immediate supervisor within 5 working days</li>
                <li>If unresolved, submit written appeal to Department Head</li>
                <li>Department Head reviews and responds within 10 working days</li>
                <li>Final appeal to HR Director if still unsatisfied</li>
                <li>HR Director's decision is final</li>
            </ol>
        </div>
    </div>
</div>

