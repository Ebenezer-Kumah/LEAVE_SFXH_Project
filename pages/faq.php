                    <ul>
                        <li><strong>Immediate family:</strong> Up to 7 working days</li>
                        <li><strong>Extended family:</strong> Up to 3 working days</li>
                        <li><strong>Close friends:</strong> Up to 1 working day</li>
                    </ul>
