            <p>Consequences for leave policy violations:</p>
            <ul>
                <li><strong>First Offense:</strong> Written warning and counseling</li>
                <li><strong>Second Offense:</strong> 3-day suspension without pay</li>
                <li><strong>Third Offense:</strong> Termination of employment</li>
                <li><strong>Severe Cases:</strong> Immediate disciplinary action</li>
